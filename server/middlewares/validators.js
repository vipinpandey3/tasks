exports.validateTaskCreation = [
  body("title").notEmpty().withMessage("Title is required"),
  body("status").isIn(["To Do", "In Progress", "Done", "Deleted"]).withMessage("Invalid status"),
  body('dueDate').optional().isISO8601().withMessage('Invalid dueDate formate'),
  (req, res, next) => {
    const errors = validationResult(req);
    if (!errors.isEmpty()) {
      return res.status(400).json({ errors: errors.array() });
    }
    next();
  }
];

